package projectecinesexamen;

public class Cines {
    private int idCine;
    private String nomCine;
    private String pobCine;
    
    public int getIdCine() {
        return idCine;
    }

    public void setIdCine(int idCine) {
        this.idCine = idCine;
    }

    public String getNomCine() {
        return nomCine;
    }

    public void setNomCine(String nomCine) {
        this.nomCine = nomCine;
    }

    public String getPobCine() {
        return this.pobCine;
    }

    public void setPobCine(String pobCine) {
        this.pobCine = pobCine;
    }
  
    
}
